# Необходимо найти совпадающие элементы в Списке А и в списке B

AList = [1, 5, 9, 15, 14, 11, 10, 11, 12, 2313, 1325153, 45454]
BList = [2, 4, 5, 15, 11, 2313]

CList = 0

for a in AList:
    for b in BList:
        if a == b:
            CList += 1
            print("Число:", a)
print("Количество повторений:", CList)